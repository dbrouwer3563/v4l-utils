#include "trace-info.h"

struct definition {
	unsigned long val;
	const char *str;
};

static std::string val2s(unsigned long val, const definition *def)
{
	std::string s;

	if (val == 0)
		return s;

	while ((def->val) && (def->val != val))
		def++;

	if (def->val == val)
		s = def->str;

	return s;
}

